# ================================================================
#                        Регулярные выражения (RegEx)
# ================================================================

# В Python для работы с регулярными выражениями используется модуль `re`.
# Он содержит набор функций, которые позволяют искать, заменять и анализировать строки по шаблону.

import re

# -----------------------------
# Функции модуля re
# -----------------------------
# findall() - возвращает список всех совпадений с шаблоном
# Пример: все цифры в строке
# re.findall(r"\d", "Я имею 2 яблока и 3 апельсина") -> ['2', '3']

# search() - возвращает объект Match, если есть совпадение где-либо в строке
# Пример:
# match = re.search(r"яблоко", "У меня есть яблоко")
# if match:
#     print("Совпадение найдено!")

# split() - разбивает строку по шаблону и возвращает список
# Пример:
# re.split(r"\s", "Разделить эту строку по пробелам") -> ['Разделить', 'эту', 'строку', 'по', 'пробелам']

# sub() - заменяет совпадения с шаблоном на заданную строку
# Пример:
# re.sub(r"яблоко", "груша", "У меня есть яблоко") -> "У меня есть груша"

# -----------------------------
# Метасимволы
# -----------------------------
# Метасимволы имеют специальное значение в RegEx

# []      - набор символов, например "[a-m]" означает любой символ от a до m
# \       - сигнал специальной последовательности или экранирования спецсимволов, например "\d" - цифра
# .       - любой символ, кроме перевода строки
# ^       - начало строки, например "^hello"
# $       - конец строки, например "planet$"
# *       - 0 или более повторений, например "he.*o"
# +       - 1 или более повторений, например "he.+o"
# ?       - 0 или 1 повторение, например "he.?o"
# {}      - точное количество повторений, например "he.{2}o"
# |       - или, например "falls|stays"
# ()      - группировка и захват совпадений

# -----------------------------
# Флаги
# -----------------------------
# Флаги позволяют менять поведение шаблона

# re.ASCII / re.A         - использовать только ASCII-символы
# re.DEBUG                - выводить отладочную информацию
# re.DOTALL / re.S        - . совпадает с любым символом, включая перенос строки
# re.IGNORECASE / re.I     - регистронезависимое сопоставление
# re.MULTILINE / re.M      - ^ и $ работают для каждой строки
# re.NOFLAG               - без флагов
# re.UNICODE / re.U        - использовать Unicode-символы (по умолчанию Python 3)
# re.VERBOSE / re.X        - позволяет писать шаблон с комментариями и пробелами

# -----------------------------
# Специальные последовательности
# -----------------------------
# \A   - совпадение только в начале строки
# \b   - граница слова, начало или конец слова
# \B   - НЕ граница слова
# \d   - цифра (0-9)
# \D   - НЕ цифра
# \s   - пробельный символ
# \S   - НЕ пробельный символ
# \w   - "слово" (буквы, цифры, _)
# \W   - НЕ "слово"
# \Z   - конец строки

# Примеры использования спецпоследовательностей:

# r"\bain"   -> "ain" в начале слова
# r"ain\b"   -> "ain" в конце слова
# r"\Bain"   -> "ain" не в начале слова
# r"ain\B"   -> "ain" не в конце слова
# "\d"       -> любая цифра
# "\D"       -> любой символ кроме цифры
# "\s"       -> пробел, табуляция, перенос строки
# "\S"       -> любой символ кроме пробельного
# "\w"       -> буква, цифра или _
# "\W"       -> любой символ кроме буквы, цифры или _
# "\AThe"    -> строка начинается с "The"
# "Spain\Z"  -> строка заканчивается на "Spain"

# ==========================
# Важные моменты:
# ==========================
# 1. В Python рекомендуется использовать "raw strings" для RegEx: r"шаблон"
#    Это позволяет избежать двойного экранирования (например, r"\d" вместо "\\d")
# 2. re.findall возвращает все совпадения, а re.search только первое
# 3. re.sub можно использовать для массовой замены по шаблону
# 4. Метасимволы и спецпоследовательности можно комбинировать для сложных шаблонов

# ================================================================
#                            Sets (Наборы символов)
# ================================================================
# В регулярных выражениях, набор символов — это набор внутри квадратных скобок []
# Он позволяет искать совпадение с **любым символом из набора**.

# -----------------------------
# Примеры наборов
# -----------------------------

# [arn] 
# Найдет любой один символ из набора: 'a', 'r' или 'n'
# Пример:
# re.findall(r"[arn]", "rain") -> ['r', 'a', 'n']

# [a-n]
# Найдет любой символ от 'a' до 'n' по алфавиту (нижний регистр)
# Пример:
# re.findall(r"[a-n]", "hello world") -> ['h', 'e', 'l', 'l', 'd']

# [^arn]
# ^ внутри скобок обозначает отрицание. Найдет любой символ **кроме** 'a', 'r', 'n'
# Пример:
# re.findall(r"[^arn]", "rain") -> ['i']

# [0123]
# Найдет любую цифру из набора: 0, 1, 2, 3
# Пример:
# re.findall(r"[0123]", "2015") -> ['2', '0', '1']

# [0-9]
# Найдет любую цифру от 0 до 9
# Пример:
# re.findall(r"[0-9]", "Room 101") -> ['1', '0', '1']

# [0-5][0-9]
# Найдет любые двухзначные числа от 00 до 59
# Пример:
# re.findall(r"[0-5][0-9]", "09, 15, 67") -> ['09', '15', '67'] 
# Обратите внимание: '67' не попадет, так как первая цифра 6 > 5

# [a-zA-Z]
# Найдет любой символ от a до z (нижний регистр) или от A до Z (верхний регистр)
# Пример:
# re.findall(r"[a-zA-Z]", "Hello123") -> ['H', 'e', 'l', 'l', 'o']

# [+]
# Специальные символы внутри [] теряют своё специальное значение.
# Например, + внутри набора [] воспринимается как обычный символ '+'
# Пример:
# re.findall(r"[+]", "2+3=5") -> ['+']

# -----------------------------
# Важные моменты про наборы
# -----------------------------
# 1. Внутри [] большинство метасимволов (+, *, ., |, (), $, {}) теряют своё специальное значение.
# 2. Диапазоны [a-z], [0-9] удобно использовать для упрощения шаблонов.
# 3. Отрицание [^...] — полезно для поиска всего, что **не входит** в набор.
# 4. Можно комбинировать наборы и специальные последовательности:
#    Пример: r"[A-Za-z0-9]" — любой буквеннo-цифровой символ (аналог \w)

